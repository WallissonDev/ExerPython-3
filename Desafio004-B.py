a = input('Digite algo:')
print('O tipo primitivo desse algo é:', type(a))
print('Esse algo é númerico:',a.isnumeric())
print('Esse algo é alfabético', a.isalpha())
print('Esse algo está capitalizado:', a.istitle())
print('Esse algo está em maiúsculo?', a.isupper())

